int		ft_str_is_printable(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (33 <= str[i] && str[i] <= 126)
		{
			str++;
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
